package org.example;

public class Calculadora {

    public double somaDoisNumerosInteiros(double n1, double n2) {
        return n1 + n2;
    }

    public double subtraiDoisNumerosInteiros(double n1, double n2) {
        return n1 - n2;
    }

    public double multiplicaDoisNumerosInteiros(double n1, double n2) {
        return n1 * n2;
    }

    public double divideDoisNumerosInteiros(double n1, double n2) {
        return n1 / n2;
    }

}
